
public class Cor {
	
	private String cor;
	private String corfonte;
	
	public Cor(String c, String f) {
		cor = c;
		corfonte =f;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public String getCorfonte() {
		return corfonte;
	}

	public void setCorfonte(String corfonte) {
		this.corfonte = corfonte;
	}
	
	

}
